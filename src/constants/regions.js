export function findProvinceByCity(cityName) {
  if (!cityName) return REGIONS[0].province;
  const region = REGIONS.find(r => r.cities.includes(cityName));
  return region?.province ?? REGIONS[0].province;
}

export function getCitiesForProvince(provinceName) {
  return REGIONS.find(r => r.province === provinceName)?.cities ?? REGIONS[0].cities;
}

